    if sys.version_info < (3, 8):
        print(red("❌ Python 3.8+ required!"))
        sys.exit(1)
    print(f"  {green('✓')} Python {sys.version_info.major}.{sys.version_info.minor}.{sys.version_info.micro}")

    # Install packages
    install_packages()

    # Load existing .env
    env = load_env()

    # Ask whether to set up keys
    print(f"\n  {bold('Do you want to set up / update API keys?')}")
    print(f"  (Recommended for first-time setup or to add new keys)")
    try:
        ans = input("  Setup keys now? [Y/n] → ").strip().lower()
    except (KeyboardInterrupt, EOFError):
        ans = "n"

    if ans != "n":
        env = setup_api_keys(env)
        env = admin_setup(env)

    # Set environment variables from .env for this process
    for k, v in env.items():
        os.environ.setdefault(k, v)

    # Open browser
    threading.Thread(target=open_browser, daemon=True).start()

    # Start server
    os.chdir(str(ROOT))
    sys.path.insert(0, str(ROOT))

    print(f"\n{bold('═' * 60)}")
    print(f"  {green('🚀 Starting OmniRecon AI Backend...')}")
