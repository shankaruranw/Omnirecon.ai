    print(f"  {yellow('Press Ctrl+C to stop')}")
    print(f"{bold('═' * 60)}\n")

    try:
        from backend.server import main as server_main
        server_main()
    except KeyboardInterrupt:
        print(f"\n\n  {yellow('👋 Shutting down OmniRecon AI...')}")
