def setup_api_keys(env: dict) -> dict:
    """Interactive wizard to configure API keys."""
    print(f"\n{bold('═' * 60)}")
    print(f"{bold('  🔑  API KEY SETUP WIZARD')}")
    print(f"{bold('═' * 60)}")
    print(f"""
  API keys make tools produce {green('REAL')} results instead of simulations.
  All keys below are {green('FREE')} to register.
  Press {cyan('Enter')} to skip any key (tool will use simulation).
  Press {cyan('Q')} at any prompt to finish and start the server.
    """)

    changed = False
    for item in API_KEYS:
        k        = item["key"]
        current  = env.get(k, "")
        is_set   = bool(current)

        status = green("✓ Already set") if is_set else red("✗ Not set (simulation)")
        cost   = green(item["cost"]) if "FREE" in item["cost"] else yellow(item["cost"])

        print(f"  {bold(item['name'])}")
        print(f"    Status   : {status}")
        print(f"    Cost     : {cost}")
        print(f"    Tools    : {cyan(item['tools'])}")
        print(f"    Register : {item['url']}")

        if is_set:
            prompt = f"    New key  : [keep current, press Enter to keep / type new key] → "
        else:
            prompt = f"    Your key : [press Enter to skip, type key to activate] → "

        try:
            val = input(prompt).strip()
        except (KeyboardInterrupt, EOFError):
            val = ""

        if val.lower() == "q":
            print(f"\n  {yellow('Skipping remaining keys and starting server...')}\n")
            break

        if val:
            env[k] = val
            changed = True
            print(f"    {green('✓ Key saved!')}")
        elif is_set:
            print(f"    {cyan('→ Keeping existing key.')}")
        else:
            print(f"    {yellow('→ Skipped — tool will use simulation.')}")

        print()

    if changed:
        save_env(env)
        print(f"  {green('✓ API keys saved to .env file.')}\n")

    return env


